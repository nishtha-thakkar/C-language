# include <stdio.h>

int main(){
    printf("hello \nworld!");
    printf("\nhello \tkitty!");


    //format specifier

    int a = 10;
    float b = 10.5;
    char c = 'a';
    char  d [] = "ancd";



    printf ("\n%d", a);
    printf ("\n%f", b);
    printf ("\n%c", c);
    printf ("\n%s", d);
}



